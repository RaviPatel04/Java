public class Main {
    public static void main(String[] args) {
        System.out.println("Hello world!");
        int n=5;
        int i;
        for (i=0;i<n;i++){
            for (int j=0; j<=i ;j++) {
                System.out.print("* ");
            }
            System.out.print("\n");
        }

    }

}